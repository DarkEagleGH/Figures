public abstract class FiguresType {

    protected static String getCIRCLE() {
        return "Круг";
    }

    protected static String getTRIANGLE() {
        return "Тругольник";
    }

    protected static String getRECTANGLE() {
        return "Прямоугольник";
    }

    protected static String getSQUARE() {
        return "Квадрат";
    }
}
